function [dxdt] = LoadFunc1(t, x, ut, uq)

u = interp1(ut, uq, t);

a1 = 1.751512162713100;
b1 = -4.453880943586535e+02;
c1 = 3.646354244414281e+02;
c2 = 5.582403740407030e+02;
c3 = -65.902626460266280;
c4 = 1.995230055911658;

dx1 = a1*x(2);
dx2 = b1*x(3);
dx3 = c1*x(1)+c2*x(2)+c3*x(3)+c4*u;

dxdt = [dx1;dx2;dx3];
end

